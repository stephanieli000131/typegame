public class RecordScore {
    private int score;
    private String name;
    private String time;

    public Integer getScore() {
        return score;
    }
    public String getName(){
        return name;
    }
    public String getTime(){
        return time;
    }
    public void setScore(int tempscore){
        score = tempscore;
    }
    public void setName(String tempname){
        name = tempname;
    }
    public void setTime(String temptime){
        time = temptime;
    }
}
